        public enum TableCols
        {
            FRIENDLY_NAME = 0,
            XML_TAG_ID = 1,
            DATA_TYPE = 2,
            UNIQUE = 3,
            ALLOW_NULL = 4,
            PRIMARY_KEY = 5
        }

        public static DataTable CreateAtParTableDefn(string[,] pTableDefn, string pTableType)
        {
            try
            {
                DataTable _dt = new DataTable(pTableType);
                short k = 1;

                for (int i = 0; i <= pTableDefn.GetLength(0) - 1; i++)
                {
                    if (pTableDefn[i, Convert.ToInt32(TableCols.PRIMARY_KEY)] == "Y")
                    {
                        k += 1;
                    }
                }

                DataColumn[] _dC = new DataColumn[k + 1];
                k = 0;

                for (int i = 0; i <= pTableDefn.GetLength(0) - 1; i++)
                {
                    _dt.Columns.Add(AddDataColumn(pTableDefn[i, Convert.ToInt32(TableCols.FRIENDLY_NAME)],
                                                  pTableDefn[i, Convert.ToInt32(TableCols.XML_TAG_ID)],
                                                  pTableDefn[i, Convert.ToInt32(TableCols.DATA_TYPE)],
                                                  pTableDefn[i, Convert.ToInt32(TableCols.UNIQUE)],
                                                  pTableDefn[i, Convert.ToInt32(TableCols.ALLOW_NULL)]));

                    if (pTableDefn[i, Convert.ToInt32(TableCols.PRIMARY_KEY)] == "Y")
                    {
                        _dC[k] = _dt.Columns[i];
                        k += 1;
                    }
                }

                _dt.PrimaryKey = _dC;

                return _dt;

            }
            catch (Exception ex)
            {
                throw ex;
            }
        }
        private static DataColumn AddDataColumn(string FriendlyName, string XMLTagID, string DataType, string Unique, string AllowNull)
        {
            DataColumn functionReturnValue = default(DataColumn);

            try
            {
                DataColumn column = default(DataColumn);
                column = new DataColumn();
                column.Caption = FriendlyName;
                column.DataType = System.Type.GetType(DataType);
                column.ColumnName = XMLTagID;
                column.AllowDBNull = (AllowNull == "Y" ? true : false);
                column.Unique = (Unique == "Y" ? true : false);

                functionReturnValue = column;
            }
            catch (Exception ex)
            {
                throw ex;
            }
            return functionReturnValue;

        }
